    "import os\n",
    "import numpy as np\n",
    "import cv2\n",
    "import tensorflow as tf\n",
    "from tensorflow.keras.models import model_from_json, Sequential\n",
    "from tensorflow.keras.preprocessing import image\n",
    "\n",
    "print(\"[INFO] TensorFlow version:\", tf.__version__)\n",
    "\n",
    "\n",
    "model_json_path = \"Facial Expression Recognition.json\"\n",
    "model_weights_path = \"Facial_Expression.weights.h5\"\n",
    "\n",
    "if not os.path.exists(model_json_path):\n",
    "    print(f\"[ERROR] Model JSON file '{model_json_path}' not found.\")\n",
    "    exit()\n",
    "\n",
    "if not os.path.exists(model_weights_path):\n",
    "    print(f\"[ERROR] Model weights file '{model_weights_path}' not found.\")\n",
    "    exit()\n",
    "\n",
    "\n",
    "try:\n",
    "    print(\"[INFO] Loading model architecture...\")\n",
    "    with open(model_json_path, \"r\") as f:\n",
    "        model_json = f.read()\n",
    "    model = model_from_json(model_json, custom_objects={'Sequential': Sequential})\n",
    "    print(\"[INFO] Model architecture loaded successfully.\")\n",
    "except Exception as e:\n",
    "    print(f\"[ERROR] Failed to load model architecture: {e}\")\n",
    "    exit()\n",
    "\n",
    "\n",
    "try:\n",
    "    model.load_weights(model_weights_path)\n",
    "    print(\"[INFO] Model weights loaded successfully.\")\n",
    "except Exception as e:\n",
    "    print(f\"[ERROR] Failed to load model weights: {e}\")\n",
    "    exit()\n",
    "\n",
    "\n",
    "cascade_path = cv2.data.haarcascades + 'haarcascade_frontalface_default.xml'\n",
    "if not os.path.exists(cascade_path):\n",
    "    print(f\"[ERROR] Haar cascade file '{cascade_path}' not found.\")\n",
    "    exit()\n",
    "\n",
    "face_haar_cascade = cv2.CascadeClassifier(cascade_path)\n",
    "print(\"[INFO] Haar cascade loaded successfully.\")\n",
    "\n",
    "\n",
    "cap = cv2.VideoCapture(0)\n",
    "if not cap.isOpened():\n",
    "    print(\"[ERROR] Unable to access the webcam.\")\n",
    "    exit()\n",
    "\n",
    "emotions = ('angry', 'disgust', 'fear', 'happy', 'sad', 'surprise', 'neutral')\n",
    "print(\"[INFO] Webcam started. Press 'q' to exit.\")\n",
    "\n",
    "while True:\n",
    "    ret, test_img = cap.read()\n",
    "    if not ret:\n",
    "        print(\"[WARNING] Frame capture failed, skipping...\")\n",
    "        continue\n",
    "\n",
    "    gray_img = cv2.cvtColor(test_img, cv2.COLOR_BGR2GRAY)\n",
    "    faces_detected = face_haar_cascade.detectMultiScale(gray_img, scaleFactor=1.32, minNeighbors=5)\n",
    "\n",
    "    for (x, y, w, h) in faces_detected:\n",
    "        cv2.rectangle(test_img, (x, y), (x + w, y + h), (255, 0, 0), thickness=7)\n",
    "        roi_gray = gray_img[y:y + h, x:x + w]\n",
    "\n",
    "        try:\n",
    "            roi_gray = cv2.resize(roi_gray, (48, 48))\n",
    "        except Exception as e:\n",
    "            print(f\"[WARNING] Face crop resize failed: {e}\")\n",
    "            continue\n",
    "\n",
    "        img_pixels = image.img_to_array(roi_gray)\n",
    "        img_pixels = np.expand_dims(img_pixels, axis=0)\n",
    "        img_pixels /= 255.0\n",
    "\n",
    "        try:\n",
    "            predictions = model.predict(img_pixels)\n",
    "            max_index = np.argmax(predictions[0])\n",
    "            predicted_emotion = emotions[max_index]\n",
    "            cv2.putText(test_img, predicted_emotion, (x, y - 10),\n",
    "                        cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 0, 255), 2)\n",
    "        except Exception as e:\n",
    "            print(f\"[ERROR] Prediction failed: {e}\")\n",
    "\n",
    "    resized_img = cv2.resize(test_img, (1000, 700))\n",
    "    cv2.imshow('Facial Emotion Analysis', resized_img)\n",
    "\n",
    "    if cv2.waitKey(10) == ord('q'):\n",
    "        print(\"[INFO] Exiting on user request...\")\n",
    "        break\n",
    "\n",
    "cap.release()\n",
    "cv2.destroyAllWindows()"
